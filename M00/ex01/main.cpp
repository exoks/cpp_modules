int	main(void)
{
	PhoneBook		phoneBook;
	std::string	line;

	while(!std::cin.eof())
	{
		std::cout << std::endl << PHONEBOOK_PROMPT;
		getline(std::cin, line);
		if (line == "ADD")
			phoneBook.addContact();
		else if (line == "SEARCH")
			phoneBook.searchContact();
		else if (line == "EXIT" || std::cin.eof())
			break ;
		else
			std::cout << "PhoneBook: " << INVALID_CMD << std::endl;
	}
	return (EXIT_SUCCESS);
}
